// While loop continues iterating until the condition is true or reached 

// Initializing the variable ramadanDays to 0. Ramadan starts from day 0 and lasts for 30 days.
let ramadanDays = 0;

// The while loop iterates as long as ramadanDays is less than or equal to 30.
while (ramadanDays <= 30) {
    // Inside the loop body: 
    // 1. Print the current value of ramadanDays to the console.
    console.log(ramadanDays);
    
    // 2. Increment the value of ramadanDays by 1 for each iteration.
    ramadanDays++;
}

// In this context, the loop ensures that ramadanDays increments correctly until it reaches 30.